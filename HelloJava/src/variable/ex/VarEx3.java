package variable.ex;

public class VarEx3 {
    public static void main(String[] args) {
        long lvar = 10000000000L;
        System.out.println(lvar);
        System.out.println("lvar = " + lvar);

        boolean bvar = true;
        System.out.println(bvar);
        System.out.println("bvar = " + bvar);
        System.out.println("VarEx3.main");
    }
}
